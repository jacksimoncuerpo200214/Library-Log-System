    private void updatebookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebookActionPerformed
        try {
            String title = booktitle.getText().trim();
            String authorName = author.getText().trim();
            String publishedYearStr = publishedyear.getText().trim();
            String genreStr = genre.getText().trim();
            String copiesAvailableStr = copiesavailable.getText().trim();

            if (title.isEmpty() || authorName.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please fill in the required fields: Title and Author.");
                return;
            }

            int publishedYear = 0;
            if (!publishedYearStr.isEmpty()) {
                try {
                    publishedYear = Integer.parseInt(publishedYearStr);
                } catch (NumberFormatException e) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Published Year must be a valid number.");
                    return;
                }
            }

            int copiesAvailable = 1;
            if (!copiesAvailableStr.isEmpty()) {
                try {
                    copiesAvailable = Integer.parseInt(copiesAvailableStr);
                } catch (NumberFormatException e) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Copies Available must be a valid number.");
                    return;
                }
            }

            config.dbConnect db = new config.dbConnect();

            String updateSql = "UPDATE book SET Title = '" + title + "', Author = '" + authorName + "', PublishedYear = "
                    + (publishedYear == 0 ? "NULL" : publishedYear) + ", Genre = '" + genreStr + "', CopiesAvailable = " + copiesAvailable
                    + " WHERE BookID = " + bookId;

            int result = db.InsertData(updateSql);
            if (result == 1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Book updated successfully!");
                new manageBooks().setVisible(true);
                this.dispose();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Failed to update book. Please try again.");
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_updatebookActionPerformed

