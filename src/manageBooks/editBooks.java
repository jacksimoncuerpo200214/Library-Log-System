    private void loadBookDetails(int bookId) {
        try {
            config.dbConnect db = new config.dbConnect();
            String query = "SELECT Title, Author, PublishedYear, Genre, CopiesAvailable FROM book WHERE BookID = " + bookId;
            java.sql.ResultSet rs = db.getData(query);
            if (rs.next()) {
                this.booktitle.setText(rs.getString("Title"));
                this.author.setText(rs.getString("Author"));
                this.publishedyear.setText(rs.getString("PublishedYear"));
                this.genre.setText(rs.getString("Genre"));
                this.copiesavailable.setText(rs.getString("CopiesAvailable"));
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Book details not found.");
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error loading book details: " + e.getMessage());
        }
    }
    
