    private void editUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editUserActionPerformed
        int selectedRow = userstable.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a user to edit.");
            return;
        }
        javax.swing.table.TableModel model = userstable.getModel();
        String username = model.getValueAt(selectedRow, 0).toString();
        String firstname = model.getValueAt(selectedRow, 1).toString();
        String lastname = model.getValueAt(selectedRow, 2).toString();
        String email = model.getValueAt(selectedRow, 3).toString();
        String phone = model.getValueAt(selectedRow, 4).toString();
        String address = model.getValueAt(selectedRow, 5).toString();

        editUser editUserForm = new editUser(username, firstname, lastname, email, phone, address);
        editUserForm.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_editUserActionPerformed

