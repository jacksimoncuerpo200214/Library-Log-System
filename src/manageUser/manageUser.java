    private void loadUsers() {
        try {
            config.dbConnect db = new config.dbConnect();
            String query = "SELECT Username, FirstName, LastName, Email, Phone, Address FROM user";
            java.sql.ResultSet rs = db.getData(query);

            // Define table columns
            String[] columns = {"Username", "First Name", "Last Name", "Email", "Phone", "Address"};
            javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(columns, 0);

            while (rs.next()) {
                String username = rs.getString("Username");
                String firstname = rs.getString("FirstName");
                String lastname = rs.getString("LastName");
                String email = rs.getString("Email");
                String phone = rs.getString("Phone");
                String address = rs.getString("Address");

                Object[] row = {username, firstname, lastname, email, phone, address};
                model.addRow(row);
            }
            userstable.setModel(model);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Failed to load users: " + e.getMessage());
        }
    }

