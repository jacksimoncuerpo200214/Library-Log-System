    private void loadBorrowedBooks() {
        try {
            config.usersession session = config.usersession.getInstance();
            int userId = session.getId();
            System.out.println("Loading borrowed books for userId: " + userId);
            config.dbConnect db = new config.dbConnect();
            String query = "SELECT b.BorrowID, bk.Title AS BookTitle, b.BorrowDate, b.ReturnDate,b.Status FROM borrow b JOIN book bk ON b.BookID = bk.BookID WHERE b.UserID = " + userId;
            java.sql.ResultSet rs = db.getData(query);

            // Set table model
            String[] columnNames = {"Borrow ID", "Book Title", "Borrow Date", "Return Date", "Status"};
            javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(columnNames, 0);

            int rowCount = 0;
            while (rs.next()) {
                int borrowId = rs.getInt("BorrowID");
                String bookTitle = rs.getString("BookTitle");
                String borrowDate = rs.getString("BorrowDate");
                String returnDate = rs.getString("ReturnDate");
                String status = rs.getString("Status");
                Object[] row = {borrowId, bookTitle, borrowDate, returnDate, status};
                model.addRow(row);
                rowCount++;
            }
            System.out.println("Number of borrowed books loaded: " + rowCount);
            borrowedbooks.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading borrowed books: " + e.getMessage());
        }
    }

