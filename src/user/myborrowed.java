    private void viewborrowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewborrowActionPerformed
        try {
            int selectedRow = borrowedbooks.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Please select a borrowed book to view its receipt.");
                return;
            }
            int borrowId = Integer.parseInt(borrowedbooks.getValueAt(selectedRow, 0).toString());
            new utils.myborrowedreceipt(borrowId).setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error opening receipt: " + e.getMessage());
        }
    }//GEN-LAST:event_viewborrowActionPerformed

